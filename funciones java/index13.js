function determinarSigno(numero) {
    if (numero > 0) {
        console.log("El número es positivo.");
    } else if (numero < 0) {
        console.log("El número es negativo.");
    } else {
        console.log("El número es cero.");
    }
}


determinarSigno(5);
determinarSigno(-3);  
determinarSigno(0); 
